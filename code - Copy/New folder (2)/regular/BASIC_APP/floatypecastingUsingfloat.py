#float type casting using float(x) -> float

#int -> float
x=12
y=float(x)
print( type(x), " >>> ",type(y))
print(x, " >>> ",y)
print("--------------------------------")

#complex -> float
x=10+20j
#y=float(x) TypeError

#str -> float
x="12.3"
y=float(x)
print( type(x), " >>> ",type(y))
print(x, " >>> ",y)
print("--------------------------------")

#bool -> float
x=False
y=float(x)
print( type(x), " >>> ",type(y))
print(x, " >>> ",y)
print("--------------------------------")

















