#string type casting using str(x) -> str
#int - > str
x=10
y=str(x)
print(type(x)," >> ",type(y))
print(x, " >>> ",y)
print("==================")

#float -> str
x=12.12
y=str(x)
print(type(x)," >> ",type(y))
print(x, " >>> ",y)
print("==================")

#complex -> str
x=10+20j
y=str(x)
print(type(x)," >> ",type(y))
print(x, " >>> ",y)
print("==================")

#bool -> str
x=True
y=str(x)
print(type(x)," >> ",type(y))
print(x, " >>> ",y)
print("==================")
























