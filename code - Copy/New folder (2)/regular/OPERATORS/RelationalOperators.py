''' Relational Operators are used to
check the relationship between 2 operands
and it will return bool value True or False .
            >, >=, <, <=, ==, !=   
'''

a,b=10,20

print("a>b ? : ",a>b) #F
print("a>=b ? : ",a>=b)  #F

print("a<b ? : ",a<b)  #T
print("a<=b ? : ",a<=b) #T

print("a==b ? : ",a==b) #F
print("a!=b ? : ",a!=b)  #T

