'''
print( )
   - It will print the result on the screen and it will throw
     the cursor to the next line
   - Reason is default value of end attribute in print( )
     is '\n'  Mean new line 

     Syn: print(v1,v2,v3,.....,vn,sep=' ',end='\n')     
'''

