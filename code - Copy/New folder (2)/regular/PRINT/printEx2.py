'''Note: we can process more than one value by using
print( ) but each value is sep , in print( ) where as in
result window each value is sep by space , The Reason
is default value for sep attribute in print( ) is space

         Syn: print(v1,v2,....,vn,sep=' ')
'''

x=10          #int
y=12.12    #float
z="SssiT" #str

print(x,y,z,sep=' ')  #10 12.12 SssiT
print(x,y,z)  #10 12.12 SssiT
print(x,y,z,sep=',')
print(x,y,z,sep='<<>>')
print(x,y,z,sep='')










   
